
CREATE TABLE IF NOT EXISTS car_user(
  ID INT UNSIGNED NOT NULL PRIMARY KEY AUTO_INCREMENT,
  account VARCHAR(255) DEFAULT NULL,
  password VARCHAR(255) DEFAULT NULL,
  imei VARCHAR(255) DEFAULT NULL,
  nick VARCHAR(255) DEFAULT NULL,
  u_type VARCHAR(255) DEFAULT NULL
) DEFAULT CHARSET=utf8;

CREATE TABLE IF NOT EXISTS imei_user(
  ID INT UNSIGNED NOT NULL PRIMARY KEY AUTO_INCREMENT,
  imei VARCHAR(255) DEFAULT NULL,
  account VARCHAR(255) DEFAULT NULL,
  count BIGINT DEFAULT NULL,
  u_type VARCHAR(255) DEFAULT NULL
) DEFAULT CHARSET=utf8;

CREATE TABLE IF NOT EXISTS information(
  ID INT UNSIGNED NOT NULL PRIMARY KEY AUTO_INCREMENT,
  imei VARCHAR(255) DEFAULT NULL,
  sim VARCHAR(255) DEFAULT NULL,
  collect_time VARCHAR(255) DEFAULT NULL,
  longitude VARCHAR(255) DEFAULT NULL,
  latitude VARCHAR(255) DEFAULT NULL,
  iotstate VARCHAR(255) DEFAULT NULL,
  timestamps BIGINT DEFAULT NULL,
  u_type VARCHAR(255) DEFAULT NULL
) DEFAULT CHARSET=utf8;

CREATE TABLE IF NOT EXISTS friends(
  ID INT UNSIGNED NOT NULL PRIMARY KEY AUTO_INCREMENT,
  account VARCHAR(255) DEFAULT NULL,
  friendaccount VARCHAR(255) DEFAULT NULL,
  operation VARCHAR(255) DEFAULT NULL,
  mesend VARCHAR(255) DEFAULT NULL,
  mereceive VARCHAR(255) DEFAULT NULL,
  u_type VARCHAR(255) DEFAULT NULL
) DEFAULT CHARSET=utf8;



